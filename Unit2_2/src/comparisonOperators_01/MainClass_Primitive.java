package comparisonOperators_01;

public class MainClass_Primitive{

	public static void main(String[] args){
		int num01 = 5;
		int num02 = 5;
		if(num01 == num02){
			System.out.println("num01 == num02 delivered TRUE");
		}else{
			System.out.println("num01 == num02 delivered FALSE");
		}
		
		float myFloat01 = 3.33f;
		float myFloat02 = 3.33f;
		if(myFloat01 == myFloat02){
			System.out.println("myFloat01 == myFloat02 delivered TRUE");
		}else{
			System.out.println("myFloat01 == myFloat02 delivered FALSE");
		}
	}//main
}//class
